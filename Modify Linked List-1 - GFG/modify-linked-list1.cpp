class Solution{
    public:
    struct Node* modifyTheList(struct Node *head)
    {
        //add code here.
        Node* root=head;
        vector<int> v;
        
        while(root!=NULL)
        {
            v.push_back(root->data);
            root=root->next;
        }
        
        reverse(v.begin(),v.end());
        
        for(int i=0;i<v.size()/2;i++)
        {
            v[i]-=v[v.size()-i-1];
        }
        
        root=head;
        int idx=0;
        
        while(root!=NULL)
        {
            root->data=v[idx];
            idx++;
            root=root->next;
        }
        
        return head;
    }
};

